package com.server.entity;

import org.apache.ibatis.type.Alias;

@Alias("NormalResult")
public class NormalResult {

	private String id;
	private String name;

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}